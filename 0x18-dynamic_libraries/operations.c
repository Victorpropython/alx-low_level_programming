#include <stdio.h>
/**
 * add - a function that sums two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function that subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - a function that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - a function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - a function that finds the reminder of two integers
 * @a: first integer
 * @b: second integer
 * Return: reminder d disivion of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
